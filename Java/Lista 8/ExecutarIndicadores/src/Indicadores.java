public class Indicadores {
        private int num1;
        private int num2;
        private int num3;

        public Indicadores(int num1, int num2, int num3) {
            this.num1 = num1;
            this.num2 = num2;
            this.num3 = num3;
        }

        public void ImprimirIndicadores() {
            System.out.println(num1);
            System.out.println(num2);
            System.out.println(num3);
        }
    }
